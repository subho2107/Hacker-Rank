def swap(arr, pos1, pos2):
    temp = arr[pos1]
    arr[pos1] = arr[pos2]
    arr[pos2] = temp


def largestPermutation(k, arr):
    count = 0
    hashMap = {}
    dupArr = sorted(arr, reverse=True)
    if k >= len(arr):
        return dupArr

    if arr == dupArr:
        return arr
    for pos in range(0, len(arr)):
        num = arr[pos]
        hashMap[num] = pos
    pos = 0
    length =len(arr)
    while count < k and pos < length:
        num = dupArr[pos]
        posOfBig = hashMap[num]
        if arr[pos] != length-pos:
            swap(hashMap, num, arr[pos])
            swap(arr, pos, posOfBig)
            count += 1
        pos += 1

    return arr


